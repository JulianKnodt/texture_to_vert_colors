import os
import argparse
import json
import numpy as np


#comparisons = ["uvatlas", "xatlas"]
comparisons = ["xatlas"]
our_kinds = ["planar", "dev"]

keys = [
  "max_developability",
  "max_planarity",
  "num_charts",
]

all_results = {}

for compare in comparisons:
  for o_k in our_kinds:
    our_suffix = f"match_{compare}_{o_k}"

    base = "cluster_outputs"
    our_results = sorted([
      f for f in os.listdir(base)
      if our_suffix in f and ".json" in f and not ".swp" in f
    ])

    comp = [
      f.replace(our_suffix, compare) for f in our_results
    ]

    our_vals = {}
    cmp_vals = {}

    for (o, c) in zip(our_results, comp):
      base_idx = o.index("_match")
      name = o[:base_idx]
      o = os.path.join(base, o)
      c = os.path.join(base, c)
      assert(os.path.exists(o))
      assert(os.path.exists(c)), c
      with open(o, "r") as fo:
        o = json.load(fo)
      with open(c, "r") as fc:
        c = json.load(fc)
      if o["num_charts"] != c["num_charts"]:
        print(f"Need to fix up {name}")
        continue

      for k in keys:
        if name not in our_vals: our_vals[name] = {}
        our_vals[name][k] = o[k]
        if name not in cmp_vals: cmp_vals[name] = {}
        cmp_vals[name][k] = c[k]
    all_results[(compare, o_k)] = [cmp_vals, our_vals]


def double_col(t):
  return f"\\multicolumn{{2}}{{c|}}{{ {t} }}"

def header(title):
  print("% Autogenerated (Be careful with manual edits)")
  print("\\hline")
  print(f" & & {double_col(title)} & {double_col('Ours(Planar)')} & {double_col('Ours(Developable)')} \\\\")
  print(f" & \#Charts & Planar & Dev. & Planar & Dev.  & Planar & Dev. \\\\")
  print("\\hline")

def bold_num(n): return f"\\textbf{{ $\\num{{ {n:.2e} }}$ }}"
def try_num(n):
  if type(n) != float: return n
  return f"$\\num{{ {n:.2e} }}$"

def compare_table(title,base,planar,dev):
  header(title)
  for model in base.keys():
    b = base[model]
    p = planar[model]
    d = dev[model]
    num_charts = b["num_charts"]
    assert(num_charts == p["num_charts"])
    assert(num_charts == d["num_charts"])

    b_p = b["max_planarity"]
    b_d = b["max_developability"]

    p_p = p["max_planarity"]
    p_d = p["max_developability"]

    d_p = d["max_planarity"]
    d_d = d["max_developability"]

    if b_p == min(b_p, p_p, d_p): b_p = bold_num(b_p)
    elif p_p == min(b_p, p_p, d_p): p_p = bold_num(p_p)
    elif d_p == min(b_p, p_p, d_p): d_p = bold_num(d_p)

    if b_d == min(b_d, p_d, d_d): b_d = bold_num(b_d)
    elif p_d == min(b_d, p_d, d_d): p_d = bold_num(p_d)
    elif d_d == min(b_d, p_d, d_d): d_d = bold_num(d_d)

    model_clean = model.title().replace("_", " ")
    print(f"{model_clean} & {num_charts} & {try_num(b_p)} & {try_num(b_d)} & {try_num(p_p)} & \
{try_num(p_d)} & {try_num(d_p)} & {try_num(d_d)} \\\\ \\hline")

